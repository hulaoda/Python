#.......................算法E(欧几里算法)给定两个正整数m和n,求他们的最大公因子。

m = int(input())
n = int(input())

#.......................E0[确保m>=n]如果m<n，交换m,n

if m<n:
    t=m
    m=n
    n=t
    
#.......................E1[求余数]以n除以m并令r为所得余数。
    
while(True):
    r = m % n
    
#.......................E2[余数为零?]若r=0,算法结束，n即为答案。
    
    if r==0:
        break
    
#.......................E3[减少]置 m=n,n=r,并返回E1.
    
    m = n
    n = r
print(n)

